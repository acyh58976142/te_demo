package com.hr.td.util.wiringDrawing.parameter;
/**
 * 工况比载实体类
 * @author Administrator
 *
 */
public class SpecificLoadData {
	private double vertical;//垂直比载
	private double horizontal;//水平比载
	private double comprehensive;//综合比载
	
	public double getVertical() {
		return vertical;
	}
	public void setVertical(double vertical) {
		this.vertical = vertical;
	}
	public double getHorizontal() {
		return horizontal;
	}
	public void setHorizontal(double horizontal) {
		this.horizontal = horizontal;
	}
	public double getComprehensive() {
		return comprehensive;
	}
	public void setComprehensive(double comprehensive) {
		this.comprehensive = comprehensive;
	}

}
